ortalama = int(input("Lütfen ortalamanızı giriniz: "))

if ortalama >= 85:
    print("Tebrikler, Takdir aldınız!")

elif ortalama >= 70:
    print("Tebrikler, Teşekkür aldınız!")

elif ortalama >= 50:
    print("Tebrikler, Başarı belgesi aldınız!")

else:
    print("Üzgünüz, Belge alamadınız. Daha çok çalışmalısınız!")

